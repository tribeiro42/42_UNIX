#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int	lens1;
	int	i;

	lens1 = ft_strlen(s1);
	i = 0;
	while (s2[i])
	{
		s1[lens1] = s2[i];
		lens1++;
		i++;
	}
	s1[lens1] = '\0';
	return (s1);
}
