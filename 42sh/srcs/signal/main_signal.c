#include "sh.h"

void	main_signal(void)
{
	int		i;

	i = 0;
	while (i < 31)
	{
		if (i != SIGKILL && i != SIGSTOP && i != SIGCHLD)
			signal(i, SIG_IGN);
		i++;
	}
	return ;
}
